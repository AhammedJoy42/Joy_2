// Arithmetic operator:

public class Jewel_19 {
    public static void main(String args[]){
    double a = 12;
    double b = 6;
    
    // Addition:
    System.out.println(a + b);

    // Subtraction:
    System.out.println(a - b);
    System.out.println(b - a);

    // Multiplication:
    System.out.println(a * b);
    
    // Division:
    System.out.println(a / b);
    System.out.println(b / a);
    
    // Remainder:
    System.out.println(a % b);
    System.out.println(b % a);

    
}
}
