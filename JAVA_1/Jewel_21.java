// Relational Operator:

public class Jewel_21 {
    public static void main(String args[]){
        int a = 12;
        int b = 6;
        
        System.out.println(a > b);
    
        System.out.println(a >= b);
        System.out.println(b < a);
    
        System.out.println(a <= b);
        

        System.out.println(a == b);
        System.out.println(b != a);
        
        // Remainder:
        System.out.println(a % b);
        System.out.println(b % a);
    
        
    }
    
}
