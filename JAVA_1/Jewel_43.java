// for loop _ 5:

public class Jewel_43 {
    public static void main(String args[]){
        for(int num = 1; num <= 10; num++){
            for(int multiplier = 1; multiplier <= 10; multiplier++){
                System.out.printf("%d X %d = %d \n", num, multiplier, num * multiplier);
            }
        }
    }
    
}
